#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	char *s;

	s = (char *)str;
	if (ch < 0 || ch > 255)
		return (NULL);
	while (*s != ch)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	return (s);
}
