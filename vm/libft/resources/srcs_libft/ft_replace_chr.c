#include "libft.h"

char	*ft_replace_chr(char *str, int ch1, int ch2)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ch1)
			str[i] = ch2;
		i++;
	}
	return (str);
}
