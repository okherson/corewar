#include "libft.h"

int		ft_numlen(int n)
{
	int	len;

	len = n <= 0 ? 1 : 0;
	n *= n < 0 ? -1 : 1;
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
