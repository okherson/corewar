#include <string.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	i = -1;
	while (s != NULL && f != NULL && s[++i] != '\0')
	{
		f(i, &s[i]);
	}
}
