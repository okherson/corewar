#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t		i;

	i = 0;
	if (n == 0)
		return (1);
	while (s1 != NULL && s2 != NULL && (s1[i] == s2[i]) &&
		!((s1[i] == '\0' && s2[i] != '\0') || (s1[i] != '\0' && s2[i] == '\0'))
		&& i < n)
	{
		i++;
		if ((s1[i] == '\0' && s2[i] == '\0') || (s1[i] == s2[i] && i == n - 1))
			return (1);
	}
	return (0);
}
